var dayOfWeek = 2;

switch (dayOfWeek){
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuseday");
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;
    default:
        console.log("Invalid day of week");
}
